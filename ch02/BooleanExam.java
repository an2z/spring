package ch02;

/* 논리 자료형 */
public class BooleanExam {
	public static void main(String[] args) {		
		boolean b1 = true;
		boolean b2 = false;		
		System.out.println(b1);  // b1이 지닌 값 출력
		System.out.println(b2);
		
		int n1 = 10;
		int n2 = 20;
		System.out.println(n1 < n2);
		System.out.println(n1 > n2);
	}

}
